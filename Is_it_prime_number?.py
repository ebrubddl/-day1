def prime_checker(n):
    is_prime_number=True   
    for x in range(2,number):
        if n % x==0:
          is_prime_number=False 
    if is_prime_number:
      print("It's a prime number.")
    else:
      print("It's not a prime number.")
number = int(input("Check this number: "))
prime_checker(number)
